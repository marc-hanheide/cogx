package balt.core.data;

public class FrameworkProcessIdentifier {

    private String m_idString;

    public FrameworkProcessIdentifier(String _id) {
        m_idString = _id;
    }

    /*
     * (non-Javadoc)
     * 
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (obj instanceof FrameworkProcessIdentifier) {
            return m_idString
                .equals(((FrameworkProcessIdentifier) obj).m_idString);
        }
        if (obj instanceof String) {
            return m_idString.equals(obj);
        }
        else {
            return false;
        }
    }

    /*
     * (non-Javadoc)
     * 
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        return m_idString;
    }

}
